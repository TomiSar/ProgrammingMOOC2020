luku1 = int(input("Luku1: "))
luku2 = int(input("Luku2: "))

komento = input("Komento: ")
if komento == "summa":
    print(f"\n{luku1} + {luku2} = {luku1 + luku2}")
if komento == "tulo":
    print(f"\n{luku1} * {luku2} = {luku1 * luku2}")
if komento == "erotus":
    print(f"\n{luku1} - {luku2} = {luku1 - luku2}")